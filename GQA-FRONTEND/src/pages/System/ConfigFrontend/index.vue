<template>
    <q-page padding>

        <div class="row q-gutter-md items-center" style="margin-bottom: 10px">
            <q-input style="width: 20%" v-model="queryParams.gqaOption" label="前台配置名" />
            <q-input style="width: 20%" v-model="queryParams.remark" label="描述" />
            <q-btn color="primary" @click="handleSearch" label="搜索" />
            <q-btn color="primary" @click="resetSearch" label="重置" />
        </div>

        <q-table row-key="id" separator="cell" :rows="tableData" :columns="columns" v-model:pagination="pagination"
            :rows-per-page-options="pageOptions" :loading="loading" @request="onRequest">

            <template v-slot:top="props">
                <q-btn color="primary" @click="showAddForm()" label="新增前台配置" />
                <q-space />
                <q-btn flat round dense :icon="props.inFullscreen ? 'fullscreen_exit' : 'fullscreen'"
                    @click="props.toggleFullscreen" class="q-ml-md" />
            </template>

            <template v-slot:body-cell-default="props">
                <q-td :props="props">
                    <template v-if="props.row.gqaOption === 'gqaWebLogo'">
                        <GqaAvatar :src="props.row.default" />
                    </template>
                    <template v-else>
                        {{props.row.default}}
                    </template>
                </q-td>
            </template>

            <template v-slot:body-cell-custom="props">
                <q-td :props="props" class="bg-green-1">
                    <template v-if="props.row.gqaOption === 'gqaWebLogo'">
                        <GqaAvatar :src="props.row.custom" />
                    </template>
                    <template v-else>
                        {{props.row.custom}}
                    </template>

                    <q-popup-edit v-model="props.row.custom" class="bg-green-13">
                        <!-- 使用是否字典显示 -->
                        <template v-slot v-if="props.row.gqaOption === 'gqaShowGit'">
                            自定义：{{ props.row.gqaOption }}
                            <q-option-group v-model="props.row.custom" :options="options.statusYesNo" color="primary"
                                inline>
                            </q-option-group>
                        </template>
                        <!-- LinkIco上传图标 -->
                        <template v-slot v-else-if="props.row.gqaOption === 'gqaWebLogo'">
                            自定义：{{ props.row.gqaOption }}
                            <q-file class="col" v-model="webLogoFile" max-files="1">
                                <template v-slot:prepend>
                                    <GqaAvatar :src="props.row.custom" />
                                </template>
                                <template v-slot:after v-if="webLogoFile">
                                    <q-btn dense flat color="primary" icon="cloud_upload" @click="handleUpload" />
                                </template>
                            </q-file>
                        </template>
                        <!-- 默认的输入框 -->
                        <template v-slot v-else>
                            自定义：{{ props.row.gqaOption }}
                            <q-input v-model="props.row.custom" dense autofocus clearable />
                        </template>
                    </q-popup-edit>
                </q-td>
            </template>

            <template v-slot:body-cell-status="props">
                <q-td :props="props">
                    <GqaDictShow dictName="statusOnOff" :dictCode="props.row.status" />
                </q-td>
            </template>

            <template v-slot:body-cell-stable="props">
                <q-td :props="props">
                    <GqaDictShow dictName="statusYesNo" :dictCode="props.row.stable" />
                </q-td>
            </template>

            <template v-slot:body-cell-actions="props">
                <q-td :props="props">
                    <div class="q-gutter-xs">
                        <q-btn dense color="primary" @click="handleSave(props.row)" label="保存编辑" />
                        <q-btn color="negative" @click="handleDelete(props.row)" label="删除" />
                    </div>
                </q-td>
            </template>
        </q-table>
        <add-or-edit-dialog ref="addOrEditDialog" @handleFinish="handleFinish" />
    </q-page>
</template>

<script>
import { tableDataMixin } from 'src/mixins/tableDataMixin'
import { mapActions } from 'vuex'
import addOrEditDialog from './modules/addOrEditDialog'
import { putAction, postAction } from 'src/api/manage'
import GqaDictShow from 'src/components/GqaDictShow'
import { addOrEditMixin } from 'src/mixins/addOrEditMixin'
import GqaAvatar from 'src/components/GqaAvatar'

export default {
    name: 'ConfigFrontend',
    mixins: [tableDataMixin, addOrEditMixin],
    components: {
        addOrEditDialog,
        GqaDictShow,
        GqaAvatar,
    },
    data() {
        return {
            url: {
                list: 'config-frontend/config-frontend-list',
                edit: 'config-frontend/config-frontend-edit',
                delete: 'config-frontend/config-frontend-delete',
                linkIcoUrl: 'upload/web-logo',
            },
            columns: [
                { name: 'sort', align: 'center', label: '排序', field: 'sort' },
                { name: 'gqaOption', align: 'center', label: '前台配置名', field: 'gqaOption' },
                { name: 'remark', align: 'center', label: '描述', field: 'remark' },
                { name: 'default', align: 'center', label: '默认网站前台配置', field: 'default' },
                { name: 'custom', align: 'center', label: '自定义网站前台配置', field: 'custom' },
                { name: 'status', align: 'center', label: '状态', field: 'status' },
                { name: 'stable', align: 'center', label: '系统内置', field: 'stable' },
                { name: 'actions', align: 'center', label: '操作', field: 'actions' },
            ],
            webLogoFile: null,
        }
    },
    created() {
        this.getTableData()
    },
    methods: {
        ...mapActions('storage', ['SetGqaFrontend']),
        async handleSave(row) {
            const res = await putAction(this.url.edit, row)
            if (res.code === 1) {
                this.$q.notify({
                    type: 'positive',
                    message: res.message,
                })
                this.getTableData().then(() => {
                    this.SetGqaFrontend(this.tableData)
                })
            }
        },
        handleUpload() {
            if (!this.webLogoFile) {
                this.$q.notify({
                    type: 'negative',
                    message: '请选择文件！',
                })
                return
            }
            let form = new FormData()
            form.append('file', this.webLogoFile)
            postAction(this.url.linkIcoUrl, form).then((res) => {
                if (res.code === 1) {
                    const gqaWebLogo = this.tableData.filter((item) => {
                        return item.gqaOption === 'gqaWebLogo'
                    })
                    gqaWebLogo[0].custom = res.data.records
                    this.webLogoFile = null
                    this.$q.notify({
                        type: 'positive',
                        message: '网站Logo上传成功！',
                    })
                }
            })
        },
    },
}
</script>